                            <h2>09.12.05</h2>              <a name="6"></a>              <div class="storyTitle">
                Parents Do Care                <a href="?cat=2" title="category: Edutainment"><span class="storyCategory">[
                  Edutainment                  ]</span></a>&nbsp;-&nbsp; <span class="storyAuthor">
                    bigbrainz                    -
                    &#98;&#101;&#110;&#64;b&#105;g&#98;r&#97;&#105;n&#122;&#46;co&#109;                    </span> @ <a href="index.php?m=200512#6">
                      15:41:26                    </a> </div>
              <div class="storyContent">
                
                <div align="justify">
                  The video game industry will tell you that most parents just don&#8217;t care about education anymore. Parents will gladly shell out $50 for a kill-maim-explode video game, but not for an educational one. That&#8217;s why no developer is going to bother spending millions and millions of dollars to make a decent educational video game. There&#8217;s simply no demand for them. That&#8217;s why P.J. McNealy was quoted in USA Today (May 11th, 2004) saying, &#8220;Learning multiplication tables on an XBox hasn&#8217;t exactly happened. People would rather shoot people, punch somebody or throw a football than learn math.&#8221;<br />
<br />
But that&#8217;s not really accurate. Yes, people would rather buy a great video game than a lousy educational one. But it&#8217;s equally true that they would rather buy a great educational game than a lousy non-educational game. Indeed, most parents PREFER investing in entertaining education&#8211;just look at LeapFrog and all the heavy-hitting knockoffs they&#8217;ve spawned. <br />
<br />
So why aren&#8217;t high-end educational video games selling very well? Simple. There aren&#8217;t any. While entertainment video games have kept investing more and more money into more and more mind-blowing games, educational games decided to stop innovating about 5 to 10 years ago. Who can blame parents for avoiding low-tech games with primitive gameplay, and watered-down education?<br />
<br />
Games have gotten so expensive to create now that no one is willing to actually step up the plate yet, but they will&#8211;and the visionary investors that resurrect edutainment will be smiling all the way to the bank. Is it easy to make an educational game as entertaining as a top video game AND as educational as a skilled teacher? No. It&#8217;s extremely difficult, but so is making any kind of game, so why not put in the extra effort? I&#8217;ve done it, and it&#8217;s well worth it.<br />
<br />
I think that fairly soon, educational games will become even more compelling than fantasy games. Why? Because it&#8217;s real. We already accept that more realistic graphics makes for more compelling gameplay. Similarly, more realistic data and strategy will have the same effect. Pulling a real solution from your own brain under pressure can easily be more intense than just pressing a plastic button.<br />
<br />
During my own experience with Timez Attack, every single parent or teacher who deals with multiplication has absolutely wanted a copy. Some walk away with the free version while others grab the upgrade, but the demand is there. The educational gaming market is alive and well. There&#8217;s just no one there to see it.                  </div>
