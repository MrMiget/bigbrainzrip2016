		                  <p align="center" class="title">Finishing vs Improving</p>
		                  <p align="left" class="text">Most other programs only track improvement. There's no mention of finishing and little mention of time. This is a critical oversight. First, why would a product settle for &quot;improvement&quot; when complete mastery can be obtained in a matter of hours? As an educational community we need to raise our standards so that we genuinely demand fluency, rather than improvement. </p>
		                  <p align="left" class="text">&nbsp;</p>
		                  <p align="center" class="title"><span class="text"><a name="Time" id="Time"></a></span>It's About Time</p>
