		                <td align="center"><p align="left" class="text">When it comes to statistics, the phrase that always springs to mind is &quot;Lies, damn lies, and statistics.&quot; To avoid that tendency, we work hard to be  transparent  with our research. Our data is astonishingly simple, but it's also just plain astonishing. And  game-changing for most educators. We don't need to exagerrate or cherry-pick our results, so please let us know if our efforts to be succinct leave you feeling skeptical in any way. Even better though, put Timez Attack to the test with your own students so you'll have your very own, live data&#8212;because you seriously won't believe these results until you see them for yourself, with your very own students.</p>
