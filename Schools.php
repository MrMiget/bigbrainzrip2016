		                  <p align="left" class="text">We partnered with 500 districts across the country to demonstrate the efficacy of Timez Attack. Each student was given a pre-test that simply asked them each of the multiplication facts, 2 - 12, evaluating their response for speed and accuracy,  factoring in the time required for typing. They were then given a matching post-test after completing Timez Attack. Over 500,000 students took the pre-test and then 35,000 of those students were allowed sufficient time to finish the program. A histogram of their 35,000 pre-test (blue) and post-test scores (red) is displayed below. By definition, many of the the 35,000 students who finished Timez Attack were the Fast Finishers, so their pre-test scores were much better than the national average.		                  </p>
